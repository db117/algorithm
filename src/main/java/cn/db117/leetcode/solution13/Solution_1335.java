    class Solution {
        int[][] memo;
        int[] jobDifficulty;

        public int minDifficulty(int[] jobDifficulty, int d) {
            int n = jobDifficulty.length;
            if (n < d) {
                // è‚¯å®šåˆ†ä¸äº†
                return -1;
            }
            memo = new int[n][d + 1];
            this.jobDifficulty = jobDifficulty;
            for (int[] ints : memo) {
                Arrays.fill(ints, -1);
            }
            // æ‰¾åˆ° d ä¸ªåŒºé—´ï¼Œæ¯ä¸ªåŒºé—´çš„æœ€å¤§å€¼ä¹‹å’Œæœ€å°

            return dfs(n - 1, d);
        }


        private int dfs(int i, int remain) {
            if (memo[i][remain] != -1) {
                return memo[i][remain];
            }

            int max = -1;
            if (remain == 1) {
                // å…¨éƒ¨çš„éƒ½è¦æå®Œ
                for (int j = i; j >= 0; j--) {
                    // ç›´æ¥æ‰¾å‰©ä½™çš„æœ€å¤§å€¼
                    max = Math.max(max, jobDifficulty[j]);
                }
                return memo[i][remain] = max;
            }

            int min = Integer.MAX_VALUE;
            for (int j = i; j >= remain - 1; j--) {
                // å½“å‰åŒºé—´çš„æœ€å¤§å€¼
                max = Math.max(max, jobDifficulty[j]);
                // æ‰¾æ›´å°åŒºé—´çš„æœ€å°å€¼
                min = Math.min(min, max + dfs(j - 1, remain - 1));
            }


            return memo[i][remain] = min;
        }
    }
