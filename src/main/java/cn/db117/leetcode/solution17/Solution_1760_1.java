    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public int minimumSize(int[] nums, int maxOperations) {
            Arrays.sort(nums);
            int left = 1, right = nums[nums.length - 1];

            while (left < right) {
                int mid = (left + right) / 2;
                if (bs(nums, mid, maxOperations)) {
                    // å½“å‰å€¼å¯èƒ½æ˜¯ç­”æ¡ˆ
                    right = mid;
                } else {
                    left = mid + 1;
                }
            }
            return left;
        }

        private boolean bs(int[] nums, int min, int count) {
            for (int i = nums.length - 1; i >= 0; i--) {
                int num = nums[i];
                if (num > min) {
                    count -= (num - 1) / min;
                }
                if (count < 0) {
                    return false;
                }
            }
            return true;
        }
    }
