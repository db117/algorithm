    class Solution {
        // è®°å½•ä¸¤ä¸ªæ•°æ˜¯å¦äº’è´¨
        static int[][] temp = new int[51][51];

        static {
            for (int i = 1; i <= 50; i++) {
                for (int j = 1; j <= 50; j++) {
                    if (gcd(i, j) == 1) {
                        temp[i][j] = 1;
                    }
                }
            }
        }

        List<Integer>[] tree;
        int n;
        int[] nums;
        int[] ans;

        public int[] getCoprimes(int[] nums, int[][] edges) {
            n = nums.length;
            this.nums = nums;
            ans = new int[n];
            Arrays.fill(ans, -1);

            // æ„å»ºæ ‘
            tree = new ArrayList[n];
            for (int i = 0; i < n; i++) {
                tree[i] = new ArrayList<>();
            }
            for (int[] edge : edges) {
                tree[edge[0]].add(edge[1]);
                tree[edge[1]].add(edge[0]);
            }

            // è®°å½•ç¥–å…ˆä¸­æœ€è¿‘çš„èŠ‚ç‚¹å’Œæ·±åº¦
            int[] valNodeId = new int[51];
            int[] valDepth = new int[51];
            Arrays.fill(valNodeId, -1);
            Arrays.fill(valDepth, -1);
            dfs(0, -1, 0, valNodeId, valDepth);

            return ans;
        }

        private void dfs(int cur, int pre, int depth, int[] valNodeId, int[] valDepth) {
            // æ‰¾åˆ°50ä¸ªæ•°ä¸­ä¸å½“å‰æ•°äº’è´¨çš„æœ€è¿‘çš„æ·±åº¦
            int maxDepth = -1;
            int val = nums[cur];
            for (int i = 1; i <= 50; i++) {
                if (temp[val][i] == 1 && valNodeId[i] != -1) {
                    if (valDepth[i] > maxDepth) {
                        maxDepth = valDepth[i];
                        ans[cur] = valNodeId[i];
                    }
                }
            }

            // å¤‡ä»½
            int oldNodeId = valNodeId[val];
            int oldDepth = valDepth[val];
            // æ›´æ–°å½“å‰èŠ‚ç‚¹çš„ä¿¡æ¯
            valNodeId[val] = cur;
            valDepth[val] = depth;

            for (int next : tree[cur]) {
                if (next != pre) {
                    dfs(next, cur, depth + 1, valNodeId, valDepth);
                }
            }
            // æ¢å¤
            valNodeId[val] = oldNodeId;
            valDepth[val] = oldDepth;
        }


        private static int gcd(int a, int b) {
            return b == 0 ? a : gcd(b, a % b);
        }
    }
