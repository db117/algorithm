    class Solution {
        public List<Integer> replaceNonCoprimes(int[] nums) {
            LinkedList<Integer> queue = new LinkedList<>();
            for (int num : nums) {
                while (!queue.isEmpty() && gcd(queue.getLast(), num) > 1) {// å­˜åœ¨éå…¬å› æ•°
                    num = lcm(queue.removeLast(), num);
                }
                queue.add(num);
            }
            return queue;
        }

        // æœ€å¤§å…¬çº¦æ•°
        private int gcd(int a, int b) {
            if (a == 0) {
                return b;
            }
            return gcd(b % a, a);
        }

        // æœ€å°å…¬å€æ•°
        private int lcm(int a, int b) {
            // å…ˆé™¤å†ä¹˜ã€‚é˜²æ­¢æº¢å‡º
            return a / gcd(a, b) * b;
        }
    }
