    class Solution {
        public int[] smallestSubarrays(int[] nums) {
            int n = nums.length;
            int[] ans = new int[n];
            // è®°å½•æ¯ä¸ª bit çš„ä½ç½®
            TreeSet<Integer>[] set = new TreeSet[32];
            for (int i = 0; i < 32; i++) {
                set[i] = new TreeSet<>();
            }
            for (int i = 0; i < nums.length; i++) {
                int num= nums[i];
                for (int j = 0; j < 32; j++) {
                    if ((num & (1 << j) )> 0) {
                        set[j].add(i);
                    }
                }
            }
            for (int i = 0; i < n; i++) {
                int far = i;
                for (TreeSet<Integer> treeSet : set) {
                    // æ‰¾åˆ°ç¬¬ä¸€ä¸ªå¤§äºç­‰äºå½“å‰ä½ç½®çš„ä½ç½®
                    Integer higher = treeSet.ceiling(i);
                    if (higher != null) {
                        far = Math.max(far, higher);
                    }
                }
                ans[i] = far - i + 1;
            }

            return ans;
        }
    }
