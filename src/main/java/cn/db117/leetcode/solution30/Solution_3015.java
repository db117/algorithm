    class Solution {
        public int[] countOfPairs(int n, int x, int y) {
            int[] ans = new int[n];

            Queue<Integer> queue = new LinkedList<>();
            Set<Integer> set = new HashSet<>();
            // æ¨¡æ‹Ÿä»æ¯ä¸€ä¸ªèŠ‚ç‚¹å‡ºå‘
            for (int i = 1; i <= n; i++) {
                set.clear();
                set.add(i);
                // ç›¸é‚»èŠ‚ç‚¹
                if (i + 1 <= n) {
                    queue.add(i + 1);
                    set.add(i + 1);
                }
                if (i - 1 > 0) {
                    queue.add(i - 1);
                    set.add(i - 1);
                }
                // å¤šçš„é‚£æ¡
                if (i == x && !set.contains(y)) {
                    queue.add(y);
                    set.add(y);
                }
                if (i == y && !set.contains(x)) {
                    queue.add(x);
                    set.add(x);
                }
                int cur = 0;
                while (!queue.isEmpty()) {
                    int size = queue.size();
                    for (int j = 0; j < size; j++) {
                        Integer poll = queue.poll();
                        if (cur >= n) {
                            break;
                        }
                        ans[cur]++;
                        // å¤šçš„é‚£æ¡
                        if (poll == x && !set.contains(y)) {
                            queue.add(y);
                            set.add(y);
                        }
                        if (poll == y && !set.contains(x)) {
                            queue.add(x);
                            set.add(x);
                        }
                        // ç›¸é‚»èŠ‚ç‚¹
                        if (poll - 1 > 0 && !set.contains(poll - 1)) {
                            queue.add(poll - 1);
                            set.add(poll - 1);
                        }
                        if (poll + 1 <= n && !set.contains(poll + 1)) {
                            queue.add(poll + 1);
                            set.add(poll + 1);
                        }
                    }
                    cur++;
                }
            }


            return ans;
        }
    }
