    class Solution {
        int[][] memo;
        int n;
        int[][] fruits;

        public int maxCollectedFruits(int[][] fruits) {
            // å·¦ä¸Šè§’çš„å°æœ‹å‹åªèƒ½èµ°å¯¹è§’çº¿ï¼Œä¸ç„¶åˆ°ä¸äº†ç»ˆç‚¹
            // å³ä¸Šè§’çš„ä¸èƒ½ç©¿è¿‡å·¦ä¸Šåˆ°å³ä¸‹çš„å¯¹è§’çº¿ï¼Œä¸ç„¶åˆ°ä¸äº†ç»ˆç‚¹  å·¦ä¸‹è§’çš„åŒç†
            n = fruits.length;
            memo = new int[n][n];
            this.fruits = fruits;

            for (int i = 0; i < n; i++) {
                Arrays.fill(memo[i], -1);
            }
            int ans = 0;
            // å·¦ä¸Šè§’
            for (int i = 0; i < n; i++) {
                ans += fruits[i][i];
            }
            // å³ä¸Šè§’
            ans += dfs(n - 2, n - 1);

            // ç¿»è½¬çŸ©é˜µï¼Œåœ¨èµ°ä¸€éå³ä¸Šè§’
            for (int i = 0; i < n; i++) {
                for (int j = 0; j < n; j++) {
                    fruits[i][j] = fruits[j][i];
                }
            }
            for (int i = 0; i < n; i++) {
                Arrays.fill(memo[i], -1);
            }
            return ans + dfs(n - 2, n - 1);
        }

        public int dfs(int i, int j) {
            if (j + i < n - 1 || j >= n) {
                // ä¸èƒ½è¶Šè¿‡å¯¹è§’çº¿
                return 0;
            }
            if (memo[i][j] != -1) {
                return memo[i][j];
            }
            if (i == 0) {
                return fruits[i][j];
            }
            int res = dfs(i - 1, j);
            res = Math.max(res, dfs(i - 1, j - 1));
            res = Math.max(res, dfs(i - 1, j + 1));

            return memo[i][j] = res + fruits[i][j];
        }
    }
