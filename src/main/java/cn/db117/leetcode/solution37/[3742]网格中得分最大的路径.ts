function maxPathScore(grid: number[][], k: number): number {
    const m = grid.length, n = grid[0].length;
    // 三维 dp 数组
    const dp: number[][][] = new Array(m).fill(0)
        .map((): number[][] => new Array(n).fill(0)
            .map((): number[] => new Array(k + 1).fill(-1)));

    function dfs(i: number, j: number, k: number): number {
        if (i < 0 || i >= m || j < 0 || j >= n) {
            return -Infinity;
        }
        if (k < 0) {
            return -Infinity;
        }
        if (i == 0 && j == 0) {
            // 终点
            return 0;
        }
        if (dp[i][j][k] !== -1) {
            return dp[i][j][k];
        }

        // 下一个节点的 k 值
        const nk = grid[i][j] == 0 ? k : k - 1;

        // 往左或者往上
        let res = Math.max(dfs(i - 1, j, nk), dfs(i, j - 1, nk)) + grid[i][j];

        dp[i][j][k] = res;
        return res;
    }

    const ans = dfs(m - 1, n - 1, k)
    return ans < 0 ? -1 : ans;
};
